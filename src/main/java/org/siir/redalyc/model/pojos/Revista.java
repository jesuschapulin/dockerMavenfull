package org.siir.redalyc.model.pojos;

public class Revista {
	private long clave;
	private String nombre;
	
	public long getClave() {
		return clave;
	}
	public void setClave(long clave) {
		this.clave = clave;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
}
